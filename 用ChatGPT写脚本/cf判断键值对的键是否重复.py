"""
写一个Python脚本,通过读取Python字典进行判断字典的键是否有重复的,需要读取多个字典,使用oop写.
我们使用了正则表达式 r'"(.*?)":\\s*"(.*?)"' 来匹配键和值。这个正则表达式会匹配双引号括起来的键和值，忽略键值对之间的空格或换行。findall 函数会返回所有匹配的键值对列表。

请确保文件的键值对格式仍然是有效的，并且双引号括起来的键和值之间使用冒号分隔。另外，确保输入的文件路径正确，并且文件存在。
"""

import re

class 字典检查器:
    def __init__(自己):
        自己.字典列表 = []

    def 添加字典(自己, 字典):
        自己.字典列表.append(字典)

    def 检查重复键(自己):
        所有键 = []
        重复键 = []

        for 字典 in 自己.字典列表:
            for 键 in 字典.keys():
                if 键 in 所有键:
                    重复键.append(键)
                else:
                    所有键.append(键)

        if 重复键:
            print("发现重复键：")
            for 键 in 重复键:
                print(键)
        else:
            print("未发现重复键。")


# 创建 字典检查器 实例
检查器 = 字典检查器()

# 提示用户输入包含多个字典的文件路径
文件路径 = input("请输入包含多个字典的文件路径：")

try:
    # 读取文件内容
    with open(文件路径, 'r') as 文件:
        文件内容 = 文件.read()

    # 使用正则表达式提取键和值
    键值对列表 = re.findall(r'"(.*?)":\s*"(.*?)"', 文件内容)

    # 创建字典并添加到检查器中
    for 键, 值 in 键值对列表:
        字典 = {键: 值}
        检查器.添加字典(字典)

    # 检查重复键
    检查器.检查重复键()

except FileNotFoundError:
    print("文件不存在。")